from joblib import load
import pandas as pd
from sklearn.preprocessing import LabelEncoder

model = load("Rainfall_Prediction.joblib")

# Load the model using the compatibility module

df = pd.DataFrame({
    "Location":["Sydney"],
    "MinTemp":[8.7],
    "MaxTemp":[19.7],
    "Rainfall":[4.20],
    "WindGustDir":["W"],
    "WindGustSpeed":[31],
    "WindDir9am":["W"],
    "WindDir3pm":["N"],
    "WindSpeed9am":[55],
    "WindSpeed3pm":[54],
    "Humidity9am":[1017.15],
    "Humidity3pm":[1005],
    "Pressure9am":[1003],
    "Pressure3pm":[1007],
    "Temp9am":[15.9],
    "Temp3pm":[16],
    "RainToday":["Yes"],
    "Day_of_Month":[26],
    "Month":[4]
})

label_encoder = LabelEncoder()
df['RainToday'] = df['RainToday'].map({'Yes': 1, 'No': 0})
df['Location'] = label_encoder.fit_transform(df['Location'])
df['WindDir9am'] = label_encoder.fit_transform(df['WindDir9am'])
df['WindDir3pm'] = label_encoder.fit_transform(df['WindDir3pm'])
df['WindGustDir'] = label_encoder.fit_transform(df['WindGustDir'])

predictions = model.predict(df)

print(predictions)

